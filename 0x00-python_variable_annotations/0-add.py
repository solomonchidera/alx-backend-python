"""
0-add.py

This module provides addition operation.

Functions:
- add(a: float, b: float) -> float: Add two floats and return the result.
"""


def add(a: float, b: float) -> float:
    '''
    Multiply two floats and return the result.

    Parameters:
    a: float
    b: float

    Return Value:
    float: the result of two addition of floats
    '''
    return a + b
